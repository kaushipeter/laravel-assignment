<?php

namespace App\Models;

// use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;

use Laravel\Sanctum\HasApiTokens;

/**
 * TECHNICAL COMPONENT: Laravel Eloquent Model
 * PURPOSE: To provide an object-oriented interface for the User data.
 * WHY: It replaces complex SQL queries with simple PHP methods, making the code 
 *      more readable, maintainable, and secure against SQL injection.
 * 
 * TECHNICAL COMPONENT: Laravel Sanctum (HasApiTokens)
 * PURPOSE: To handle API authentication for mobile or single-page applications.
 * WHY: It allows users to generate API tokens for secure access to protected endpoints, 
 *      solving the problem of stateless authentication.
 */
class User extends Authenticatable
{
    /** @use HasFactory<\Database\Factories\UserFactory> */
    use HasApiTokens, HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var list<string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'phone',
        'address',
        'role',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var list<string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }
    
    public function orders()
    {
        return $this->hasMany(Order::class);
    }
}
